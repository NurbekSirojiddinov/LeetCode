package leetcode.sorting;

public class MyClass {

    int number;
    int steps;

    public MyClass(int number, int steps) {
        this.number = number;
        this.steps = steps;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getSteps() {
        return steps;
    }

    public void setSteps(int steps) {
        this.steps = steps;
    }

}
